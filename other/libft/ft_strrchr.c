#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int i;
	int j;

	i = -1;
	j = -1;
	while (s[++i] != '\0')
	{
		if (s[i] == c)
			j = i;
	}
	if (c == '\0')
		j = i;
	if (j == -1)
		return (NULL);
	i = 0;
	while (i++ < j)
		s++;
	return ((char *)s);
}
