char	*ft_strrev(char *str)
{
	int		i;
	int		j;
	char	k;

	i = 0;
	j = 0;
	while (str[i])
		i++;
	while (j < i - 1)
	{
		k = str[j];
		str[j] = str[i - 1];
		str[i - 1] = k;
		i--;
		j++;
	}
	return (str);
}
