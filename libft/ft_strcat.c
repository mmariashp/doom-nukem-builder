char	*ft_strcat(char *restrict s1, const char *s2)
{
	int		i;
	int		p;

	p = 0;
	i = 0;
	while (s1[i])
		i++;
	while (s2[p])
	{
		s1[i] = s2[p];
		i++;
		p++;
	}
	s1[i] = '\0';
	return (s1);
}
