#include "libft.h"

int		ft_isxdigit(int c)
{
	if (ft_isdigit(c) == 1)
		return (1);
	if (ft_isupper(c) == 1)
		c = ft_tolower(c);
	if (c >= 'a' && c <= 'f')
		return (1);
	return (0);
}
