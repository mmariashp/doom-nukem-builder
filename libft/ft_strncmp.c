#include "libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t l)
{
	while (*s1 == *s2 && l-- && *s1 && *s2)
	{
		s1++;
		s2++;
	}
	if ((*s1 == '\0' && *s2 == '\0') || !l)
		return (0);
	return ((unsigned char)*s1 - (unsigned char)*s2);
}
